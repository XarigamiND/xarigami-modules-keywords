/**
 * delete all entries for a module - hook for ('module','remove','API')
 *
 * @param $args['objectid'] ID of the object (must be the module name here !!)
 * @param $args['extrainfo'] extra information
 * @return bool true on success, false on failure
 * @throws BAD_PARAM, NO_PERMISSION, DATABASE_ERROR
 */
function keywords_adminapi_removehook($args)
{
    extract($args);

    if (!isset($extrainfo)) {
        $extrainfo = array();
    }

    // When called via hooks, we should get the real module name from objectid
    // here, because the current module is probably going to be 'modules' !!!
    if (!isset($objectid) || !is_string($objectid)) {
        //$msg = xarML('Invalid #(1) for #(2) function #(3)() in module #(4)',
        //            'object ID (= module name)', 'admin', 'removehook', 'keywords');
        //throw new BadParameterException($args, $msg);
        // we *must* return $extrainfo for now, or the next hook will fail
        //return false;
        return $extrainfo;
    }

    $modid = xarModGetIDFromName($objectid);
    if (empty($modid)) {
        //$msg = xarML('Invalid #(1) for #(2) function #(3)() in module #(4)',
        //            'module ID', 'admin', 'removehook', 'keywords');
        //throw new BadParameterException($args, $msg);
        // we *must* return $extrainfo for now, or the next hook will fail
        //return false;
        return $extrainfo;
    }

    // Get database setup
    $dbconn = xarDBGetConn();
    $xartable = xarDBGetTables();

    $keywords = $xartable['keywords'];

    // Delete the entries
    $query = "DELETE FROM $keywords
               WHERE xar_moduleid = ?";

    $result = $dbconn->Execute($query,array($modid));
    if (!$result) {
        // we *must* return $extrainfo for now, or the next hook will fail
        //return false;
        return $extrainfo;
    }
    // Return the extra info
    return $extrainfo;
}
